#include "abstractstatut.h"



//-------------------------------------------------------------------------
//--------------------------Constructeur-----------------------------------
//-------------------------------------------------------------------------
AbstractStatut::AbstractStatut(const QString &nom, AbstractPokemon &cible) : QObject() , cible(cible)
{
    this->nom = new QString(nom);
}
//-------------------------------------------------------------------------
//------------------------Destructeur--------------------------------------
//-------------------------------------------------------------------------
AbstractStatut::~AbstractStatut() throw(){
    delete nom;
}
//-------------------------------------------------------------------------
//------------------------Public fonction----------------------------------
//-------------------------------------------------------------------------
QString AbstractStatut::getName()const{
    return *(this->nom);
}
//-------------------------------------------------------------------------
//------------------------Protected fonction-------------------------------
//-------------------------------------------------------------------------
AbstractPokemon& AbstractStatut::getCible(){
    return cible;
}
